// Font is a minimalistic monospace font.
//
// There is 256 characters; each character is 7 pixels wide and 13 pixel high,
// and is encoded with 13 bytes (one byte per line).
//
// Characters from 0x20 to 0x7e are standard ASCII (" " to "~"); the same
// characters with colors inverted are found from 0xA0 to 0xFE (i.e. with the
// high bit set). All the other characters are graphical parts that can be used
// to create a minimalistic GUI. Note that characters from 0x00 to 0x0F are
// always "see-through", i.e. their background is always transparent.
//
// Each character is a 7x13 bitmap, encoded with 13 bytes (one byte per line).
// The unused bit on each line is used to indicate if the character is one of
// the special "always see-through" characters (see the fragment shader).
//
