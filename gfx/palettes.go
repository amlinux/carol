func NewPaletteMSX2() Palette {
	p, err := NewPalette("MSX2")
	if err != nil {
		setErr("while creating MSX2 palette", err)
		return Palette(0)
	}
	for i := 0; i < 256; i++ {
		p.New("", RGBA{
			float32(i>>5) / 7.0,
			float32((i&0x1C)>>2) / 7.0,
			float32(i&0x3) / 3.0,
			1.0,
		})
	}

	return p
}

